import { type NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { signIn, useSession } from "next-auth/react";
import { useRouter } from "next/router";
import { trpc } from "../utils/trpc";
import DegreeWorks from "../components/workcard";
import { Button, Card } from "flowbite-react";

const Home: NextPage = () => {
  const { data: sessionData } = useSession();
  const router = useRouter();
  const handleClick = () => {
    if (!sessionData?.user?.emVerified) {
      router.push("/verify");
    }else {
      router.push("/degreeform");
    }
  };
  return (
    <>
      <Head>
        <title>Repositorio Icci</title>
        <meta name="Repositoro Icci" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="flex justify-center items-center h-[calc(100vh-6rem)]"> 
        <div className="w-1/5 flex justify-center items-center">
        </div>
        <div className="w-4/6 h-full mr-10 overflow-auto">
          <div className="place-self-start" >
            <DegreeWorks/>
          </div>
        </div>
        <div className="w-1/5 h-full max-w-xs flex">
          <Card className="flex w-3/4 h-1/4 max-w-xs max-h-20 justify-center items-center mt-10 bg-gray-100">
            <Button
              outline={true}
              gradientDuoTone="cyanToBlue"
              onClick={!sessionData ? () => signIn('google') : handleClick}
            >
              <Link className="font-poppins" href="/degreeform">
                Publica tu trabajo
              </Link>
            </Button>
          </Card>
        </div>
      </div>
    </>
  );
};

export default Home;


